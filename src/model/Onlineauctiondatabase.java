package model;

public class Onlineauctiondatabase {
	private String  item_id;
	private String item_name;
	private String name;
	private String email_address;
	private String amount_bid;
	private String auto_increment;
	public String getItem_id() {
		return item_id;
	}
	public void setItem_id(String item_id) {
		this.item_id = item_id;
	}
	public String getItem_name() {
		return item_name;
	}
	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getEmail_address() {
		return email_address;
	}
	public void setEmail_address(String email_address) {
		this.email_address = email_address;
	}
	public String getAmount_bid() {
		return amount_bid;
	}
	public void setAmount_bid(String amount_bid) {
		this.amount_bid = amount_bid;
	}
	public String getAuto_increment() {
		return auto_increment;
	}
	public void setAuto_increment(String auto_increment) {
		this.auto_increment = auto_increment;
	}
	
	

}
